function add(num1, num2) {
    return num1 + num2;
}

function subtr(num1, num2) {
    return num1 - num2;
}

function multipli(num1, num2) {
    return num1 * num2;
}

function division(num1, num2) {
    return num1 / num2;
}

console.log(add(5,2));
console.log(subtr(5,2));
console.log(multipli(5,2));
console.log(division(5,2));
